package Variables;

public class Variable {

	public static void main(String[] args) {

		int a = 6;
		double b = 6.9;
		char c = 'n';
		boolean d = true;
		String str = "Nick";
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(str);
		
	}

}
